# Definindo uma lista de nomes
names = ["Harry", "Ron", "Hermione", "Ginny", "Pato"]

# Printando para visualizar a lista
print(names)

#adicionando um elemento na lista -> ao final dela
names.append("Draco")

# Printando para visualizar a lista
print(names)

# Deixando a lista em ordem (palavras -> alfabética)
names.sort()

# Printando para visualizar a lista
print(names)

# Deixando a lista em ordem (palavras -> alfabética ao contrário)
names.sort(reverse=True)

# Printando para visualizar a lista
print(names)